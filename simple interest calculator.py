# Simple interest calculator 

# Principal value
P = int(input("Enter the Principal value: "))

# Rate of interest 
R = int(input("Enter the value of rate of interest (in percentage): "))

# Time 
T = int(input("Enter the value of Time (in year): "))

# Simple interest formula 
SI = (P*R*T)/100

# Print the value of SI 
print(SI)